     */
    public function destroy(Link $link)
    {
        if (Auth::id() === $link->user_id) {
          $link->delete();
          $message = "Link has been deleted!";
        } else {
          $message = "Unable to delete another users link!";
        }

        return redirect()->route('links.index')->with(["message" => $message]);
